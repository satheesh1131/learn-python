#Relational Operators: It compares the two values and returns Boolean Value(True or False).
#    '<' - Less Than operator
#    '>' - Greater Than operator
#    '<=' - Less Than Or Equal operator
#    '>=' - Greater Than Or Equal operator
#    '==' - Comparison operator whether the both values are equal.


#Augumented Assignment Operators:
#    '+=' - Adds the given value to the actual value of variable and assign the added value to the variable.
a=10
a+=5
print(a)
#    '-=' - Subtracts the given value from the actual value of variable and assign the subtracted value to the variable.
a=10
a-=5
print(a)
#    '*=' - Multiplies the given value to the actual value of variable and assign the multiplied value to the variable.
a=10
a*=5
print(a)
#    '/=' - Divides the actual value of variable by the given value and assign the divided value to the variable.
a=10
a/=5
print(a)
#    '%=' - Divides the actual value of variable by the given value and assign the remainder value to the variable.
a=10
a%=5
print(a)


#Logical Operators: This compare two boolean values and returns the output as boolean value.
#    'and' - It returns True only when the two compared values are true. Otherwise it returns False. 
#    'or' - It returns True when any one of the two compared values are true. Otherwise it returns False. 
#    'not' - It returns opposite of the given boolean value.
print((10>5) and (10<5))
print((10>5) or (10<5))
print(not (10<5))
 


