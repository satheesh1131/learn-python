#Conditional Statements: It executes only when the given condition is true.
#if-else Statement: It executes if block statement for True and executes else block statement for False.
#                   Here, we can give only one conditional statement to evaluate.
if (10>20) or (10<5):
    print('Condition is True')
else:
    print('Condition is False')

#elif(else-if) Statement: Here, we can give multiple conditional statement to evaluate.
a=10
if(a==5):
    print('a is equal to 5')
elif(a==10):
    print('a is equal to 10')
else:
    print('a is not equal to 5 or 10')











